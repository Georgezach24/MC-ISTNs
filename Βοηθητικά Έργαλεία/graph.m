% Δεδομένα από τον πίνακα
d = [0.5, 1, 1.3, 1.5, 1.7, 2]; % σε μέτρα (m)
p = [-53.9, -59.9, -62, -63, -65, -66]; % σε dBm

% Δημιουργία γραφήματος
figure;
plot(d, p, 'r-o', 'LineWidth', 2, 'MarkerSize', 8);

% Προσθήκη λεπτομερειών
grid on;
xlabel('Απόσταση πομπού-δέκτη (m)');
ylabel('Λαμβανόμενη ισχύς (dBm)');
title('Λαμβανόμενη Ισχύς vs Απόσταση');

% Ρύθμιση αξόνων για καλύτερη ορατότητα
axis([0.4 2.1 -68 -52]);